// Create a map for quick lookup by symbol
export const STOCK_MAP = new Map<string, StockSymbol>(
  ALL_STOCKS.map(stock => [stock.symbol.toUpperCase(), stock])
);

// Search function for autocomplete
export function searchStocks(query: string, limit: number = 10): StockSymbol[] {
  if (!query || query.length === 0) {
    return [];
  }

  const normalizedQuery = query.toUpperCase().trim();
  const results: StockSymbol[] = [];
  const seen = new Set<string>();

  // First, prioritize exact symbol prefix matches
  for (const stock of ALL_STOCKS) {
    if (stock.symbol.toUpperCase().startsWith(normalizedQuery)) {
      if (!seen.has(stock.symbol)) {
        results.push(stock);
        seen.add(stock.symbol);
      }
      if (results.length >= limit) {
        return results;
      }
    }
  }

  // Then, add company name matches
  const lowerQuery = query.toLowerCase().trim();
  for (const stock of ALL_STOCKS) {
    if (stock.name.toLowerCase().includes(lowerQuery)) {
      if (!seen.has(stock.symbol)) {
        results.push(stock);
        seen.add(stock.symbol);
      }
      if (results.length >= limit) {
        return results;
      }
    }
  }

  return results;
}
